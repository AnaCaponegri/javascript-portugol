// Conversão automática de tipos
let a = 5;
let b = "5";

{
if (a = b) {
console.log("true");
} else {
console.log("false");
}
}

{
// Comparação estrita de tipos
if (a != b) {
console.log("true");
} else {
console.log("false");   
}
}

{
// Comparação de valores diferentes
if (a !== b) {
console.log("true");
} else {
console.log("false");   
}
}   